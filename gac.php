              <h2>Designing for uncertainty</h2>

              <p>
                Presenting large amounts of artworks on screen within Collection
                index views posed an obvious challenge, in that user searches
                would return mixed sets of artworks of different dimensions and
                aspect ratios. Rather than try to shoehorn artworks into a
                strict grid, we opted for a more freeform masonry style layout
                for when numerous items would be on screen, and adapted the
                number of columns in the interface so collected artworks could
                be viewed comfortably on any device.
              </p>
