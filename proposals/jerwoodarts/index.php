			<div class="container">
		    	
		    	<div class="row">
		   	
					<div class="two-thirds column">
				  		
			  			<h1 class="proposal-section-header"><span>2.</span> Solution Methodology</h1>
			  			
					</div>
				
		    	</div>
				
			</div>
			
			<div class="container solution-step">
				
				<div class="row">
		   	
					<div class="twelve columns">
				  		
			  			<h2 class="solution-step-header"><span>Phase 1 / </span>Define</h2>
			  			
					</div>
				
		    	</div>
		    	
		    	<div class="row">
				
					<div class="one-third column">
						
						<h3>Project kick-off meeting</h3>
						<p>
							Virtual meeting to get everybody on the same page. Some of the things we'll do include defining scope, identify extent of relevant existing research and what we might need to generate, and informally review existing landing page design and site language. We'll also consider technical constraints and capabilities, with a focus on strategies for reducing page-load.
		            	</p>
		            	
					</div>
					
					<div class="one-third column">
						
						<h3>Review existing research</h3>
						<p>
  					 	A top-line summary of the Website User Review from 2021 has been provided, with further research available. We'll review the findings from this work in order to identify individual pain points and general areas of improvement.
						</p>
		            	
					</div>
					
					<div class="one-third column">
										
						<h3>Establish user needs</h3>
						<p>
					   	Define the the key things users are looking to achieve when they arrive at the NPG site homepage, and section specific landing pages. Sort in to a hierarchy of user priorities in order to start thinking about how this might influence page design.
						</p>
						
					</div>
			
		  		</div>
		  	
			</div><!-- /container -->
			
			<div class="container solution-step">
				
				<div class="row">
		   	
					<div class="twelve columns">
				  		
			  			<h2 class="solution-step-header"><span>Phase 2 / </span>Research</h2>
			  			
					</div>
				
		    	</div>
		    	
		    	<div class="row">
					
					<div class="one-half column">
					
						<h3>Site architecture review</h3>
						<p>
					   	We'll produce a sitemap that visualises the current structural organisation of the website, in order to consider how this structure can be more clearly represented at landing page level.
						</p>
						<p><strong>Deliverable: Visual sitemap in Figma with collaborative feedback invited from NPG team</strong></p>
						
					</div>
				
				
					<div class="one-half column">
						
						<h3>Current landing page review</h3>
						<p>
					   	We'll consider the current landing page template design in terms of how these are lacking hierarchical clarity and UX issues such as over-sized carousels, so we can identify past mistakes and learn from them.
						</p>
						<p><strong>Deliverable: Screen presentation in Figma with collaborative feedback invited from NPG team</strong></p>
						
					</div>
					
				</div>
				
				<div class="row-divider"></div>
				
				<div class="row">
					
					<div class="one-half column">
							
						<h3>Competitor analysis</h3>
						<p>
				  		We'll look at how comparable websites have tackled the same problems, particularly large gallery/museum sites. Are there solutions that work well, that we can think about emulating? Are there examples that offer a frustrating user experience, so we can think about what to avoid?
						</p>
						<p><strong>Deliverable: Screen presentation of competitor screen captures in Figma with collaborative feedback invited from NPG team</strong></p>
						
					</div>
					
					<div class="one-half column">
						
						<h3>User journey mapping</h3>
						<p>
					   	Based on our findings, we'll create a number of user journey scenarios to represent potential paths our users might take, from different starting points such as the NPG homepage into section-specific landing pages, and potentially into section sub-pages. We'll use these journeys to decide which screens/templates we should work on in both the wireframe and full UI design work.
						<p><strong>Deliverable: User journey map in Figma with collaborative feedback invited from NPG team</strong></p>
						
					</div>
			
		  		</div>
		  	
			</div><!-- /container -->
					
			<div class="container solution-step">
				
				<div class="row">
		   	
					<div class="twelve columns">
				  		
			  			<h2 class="solution-step-header"><span>Phase 3 / </span>Design</h2>
			  			
					</div>
				
		    	</div>
		    	
		    	<div class="row">
				
					<div class="one-third column">
						
						<h3>Wireframe prototype</h3>
						<p>
		              	A wireframe design that addresses our research phase findings will be produced for all user journey screens. We'll create a clickable, interactive presentation of this wireframe in Figma and conduct remote moderated testing with invited participants from our target audience to gauge the success of the prototype in improving UX. This is an iterative process where we can make rapid updates to the prototype based on feedback, both between individual tests and between potentially separate testing rounds, subject to the number of test participants that we can recruit.  
		            	</p>
						<p><strong>Deliverable: Interactive UX/wireframe prototype in Figma for testing with target audience volunteers. Interview screen recordings for use in iteration on wireframe design.</strong></p>
		            	
					</div>
					
					<div class="one-third column">
						
						<h3>UI prototype</h3>
		            	<p>
		              	Once we are satisfied with our wireframe prototype we'll progress to full visual UI design. Incorporating any branding guidelines and visual assets as required, this will be a second working prototype with full styling that should give a highly accurate approximation of the intended form and function of the final product once developed in code. These designs will be produced to AAA accessibility standards. Again, moderated user testing will be conducted with iteration as required to produce a final deliverable ready for development.
		            	</p>
						<p><strong>Deliverable: Interactive UI prototype in Figma for testing with target audience volunteers. Interview screen recordings for use in iteration on UI design.</strong></p>
		            	
					</div>
					
					<div class="one-third column">
			
						<h3>Design system</h3>
		            	<p>
		              	The full visual UI Design step will yield a robust Design System deliverable in Figma consisting of a Style Guide, UI Component Kit, and Icon Library, standardising visual elements such as typography, colour, spacing but also establishing reusable, modular interface components. This resource can be used by your digital researchers, designers and developers in future work, to ensure UX and UI remains consistent and coherent with new landing page design on the NPG website, and also across new digital products such as micro-sites and mobile applications.
		            	</p>
						<p><strong>Deliverable: Reusable design system files in Figma for Style Guide, UI Component Kit, and Icon Library</strong></p>
		            	
					</div>
			
		  		</div>
		  	
			</div><!-- /container -->
			
